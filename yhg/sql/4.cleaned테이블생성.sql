    평점                   FLOAT,
    체험단                  VARCHAR(10),
    구매옵션                VARCHAR(200),
    키                    FLOAT,
    몸무게                  FLOAT,
    성별                   VARCHAR(10),
    작성일                  VARCHAR(50),
    만족도                  VARCHAR(200),
    사진유무                VARCHAR(10),
    도움돼요                BIGINT,
    구매사이즈               VARCHAR(100),
    구매상세                VARCHAR(100),
    연도                   BIGINT,
    월                    BIGINT,
    일                    BIGINT,
    요일                   VARCHAR(10),
    평점_raw               BIGINT,
    만족도_응답여부             VARCHAR(10),
    사이즈                  VARCHAR(50),
    화면대비색감               VARCHAR(50),
    퀄리티                  VARCHAR(50),
    구김                   VARCHAR(50),
    두께감                  VARCHAR(50),
    신축성                  VARCHAR(50),
    색감                   VARCHAR(50),
    보온성                  VARCHAR(50),
