-- 1. cleaned_products 먼저 생성
CREATE TABLE cleaned_products (
    goodsNo               BIGINT          PRIMARY KEY,
    플랫폼                  VARCHAR(50),
    카테고리                 VARCHAR(100),
    브랜드                  VARCHAR(100),
    상품명                  VARCHAR(500),
    정가                   BIGINT,
    판매가                  BIGINT,
    할인율                  BIGINT,
    조회수                  BIGINT,
    누적판매수                BIGINT,
    리뷰수                  BIGINT,
    리뷰점수                 BIGINT,
    판매상태                 VARCHAR(20),
    sales_count_suspect   VARCHAR(10),
    sales_count_clean     FLOAT,
    view_count_suspect    VARCHAR(10),
    inactive_candidate    VARCHAR(10)
);

-- 2. cleaned_reviews 생성
