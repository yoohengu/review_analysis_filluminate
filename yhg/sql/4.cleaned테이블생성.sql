    신축성_점수               FLOAT,
    두께감_점수               FLOAT,
    구김_점수                FLOAT,
    사이즈_편차               FLOAT,
    사이즈_편차절대             FLOAT,
    화면대비색감_편차            FLOAT,
    화면대비색감_편차절대          FLOAT,
    색감_편차                FLOAT,
    색감_편차절대              FLOAT,
    노출일수                BIGINT,
    일평균_도움돼요지수           FLOAT,
    도움여부                BIGINT,
    FOREIGN KEY (goodsNo) REFERENCES cleaned_products(goodsNo)
);